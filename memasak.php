<p style="text-align: justify;"><strong>Bahan-bahan:</strong></p>

<ul>
	<li style="text-align: justify;">Sayap ayam - 8 buah</li>
	<li style="text-align: justify;">Daging ayam, dari hasil kerukan sayap - 250 gram</li>
	<li style="text-align: justify;">Tepung tapioka - 2 sdt</li>
	<li style="text-align: justify;">Telur - 1 butir</li>
	<li style="text-align: justify;">Bawang putih goreng - 2 sdt</li>
	<li style="text-align: justify;">Bawang merah goreng - 1 sdt</li>
	<li style="text-align: justify;">Saus tiram - 1 sdt</li>
	<li style="text-align: justify;">Minyak wijen - 1 sdt</li>
	<li style="text-align: justify;">Kecap manis - 2 sdt</li>
	<li style="text-align: justify;">Kaldu bubuk - 1/2 sdt</li>
	<li style="text-align: justify;">Merica - 1/2 sdt</li>
	<li style="text-align: justify;">Minyak goreng - secukupnya</li>
</ul>

<p style="text-align: justify;"><strong>Cara Membuat:</strong></p>

<ol>
	<li style="text-align: justify;">Patahkan sendi-sendi antara sayap tengah dan sayap bawah. Tarik tulang beserta dagingnya keluar dari bagian sayap yang terbuka. Lakukan secara perlahan supaya kulitnya tidak sobek. Jika masih tersangkut, potong urat dan daging dengan ujung pisau secara hati-hati, hingga terlepas dari kulitnya.</li>
	<li style="text-align: justify;">Kumpulkan daging hasil kerukan sayap. Jika belum sampai 250 gram, bisa ditambahkan dengan daging ayam fillet.</li>
	<li style="text-align: justify;">Haluskan daging bersama semua sisa bahan menggunakan food processor/chopper.</li>
	<li style="text-align: justify;">Ambil adonan daging menggunakan sendok kecil dan isikan ke dalam sayap hingga penuh. Semat ujung sayap yang terbuka dengan tusuk gigi jika perlu. Lakukan hingga adonan habis.</li>
	<li style="text-align: justify;">Kukus sayap ayam isi selama 30 menit hingga matang. Keluarkan.</li>
	<li style="text-align: justify;">Panaskan banyak minyak. Goreng sayap dengan api sedang-kecil hingga berwarna kecoklatan. Angkat dan tiriskan.</li>
	<li style="text-align: justify;">Siap disajikan.</li>
</ol>

<p style="text-align: justify;"><strong>Tips:</strong></p>

<ul>
	<li style="text-align: justify;">Sayat dengan pisau, tulang di dekat setiap persedian sayap untuk memutus urat-urat yang akan menyulitkan proses pengeluaran tulang. Lakukan dengan sangat hati-hati agar kulitnya tidak sobek.</li>
	<li style="text-align: justify;">Sayap ayam isi ini cocok untuk dibuat jadi stok frozen food. Simpan dalam freezer sayap yang telah dikukus dan goreng setiap saat akan disantap.</li>
</ul>

